# Accessing Elements of Lists

list = ['apple', 'banana', 'mango', 'orange', 'pinepal', 'grapes']

print(list[1])  # fruits at 1 from list

print(list[3])  # fruits at 3 from list

print(list[:3])  # fruits from 0 to 2

print(list[-1])  # last fruits

print(list[-2:])  # last two fruits

print(list[2:4])
